@router.post("/generate")
async def generate_text(
    request: Request, form_data: dict, user=Depends(get_verified_user)
):
    """Generic text generation using the configured task model.

    Args:
        form_data: { "prompt": str, "model": str (optional hint for model selection) }
    """
    models = request.app.state.MODELS

    # Use provided model directly if valid, otherwise fall back to task model
    model_id = form_data.get("model", "")
    if model_id and model_id in models:
        task_model_id = model_id
    else:
        fallback_id = next(iter(models), "")
        if not fallback_id:
            return JSONResponse(
                status_code=status.HTTP_400_BAD_REQUEST,
                content={"detail": "No model available"},
            )
        task_model_id = get_task_model_id(
            fallback_id,
            request.app.state.config.TASK_MODEL,
            request.app.state.config.TASK_MODEL_EXTERNAL,
            models,
        )

    prompt = form_data.get("prompt", "")
    if not prompt:
        return JSONResponse(
            status_code=status.HTTP_400_BAD_REQUEST,
            content={"detail": "prompt is required"},
        )

    payload = {
        "model": task_model_id,
        "messages": [{"role": "user", "content": prompt}],
        "stream": False,
        "metadata": {
            **(request.state.metadata if hasattr(request.state, "metadata") else {}),
            "task": "generate_text",
        },
    }

    try:
        return await generate_chat_completion(request, form_data=payload, user=user)
    except Exception as e:
        log.error("Exception in generate_text", exc_info=True)
        return JSONResponse(
            status_code=status.HTTP_400_BAD_REQUEST,
            content={"detail": str(e)},
        )
