								<div class="m-0.5">
									<div
										class="shadow-md p-0.5 rounded-full border border-white bg-gray-800 text-white group-hover:bg-gray-600 transition dark:border-gray-700 dark:bg-white dark:group-hover:bg-gray-200 dark:text-black"
									>
