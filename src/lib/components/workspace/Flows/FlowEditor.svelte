									{$i18n.t('Reset Image')}
								</button>
							{/if}
						</div>
					</div>

					{#if mode === 'create'}
						<div class="mt-2">
							<label class="block text-xs font-medium text-gray-600 dark:text-gray-400 mb-1">
								{$i18n.t('Flow ID')}
							</label>
							<div class="relative">
								<input
									type="text"
