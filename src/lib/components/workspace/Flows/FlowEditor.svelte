							<input
								type="text"
								bind:value={flowName}
								class="w-full px-2 py-1 text-sm font-semibold rounded-lg border border-gray-200 dark:border-gray-700 bg-white dark:bg-gray-800 focus:ring-2 focus:ring-blue-500 focus:border-transparent"
								placeholder={$i18n.t('Enter flow name')}
							/>
							{#if profileImageUrl !== $brandingUrls.favicon}
