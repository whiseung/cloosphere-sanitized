<script lang="ts">
	import { onMount, getContext, createEventDispatcher } from 'svelte';
	import { browser } from '$app/environment';
	import { toast } from 'svelte-sonner';
	import { goto } from '$app/navigation';
	import { trackAIGeneration, clearAITask } from '$lib/stores/ai-generation';

	const i18n = getContext('i18n');
	const dispatch = createEventDispatcher();

	import { WEBUI_NAME, user, models, showSidebar } from '$lib/stores';

	$: canWrite = $user?.role === 'admin' || $user?.permissions?.workspace?.agent_flows === 'write';
	import {
		createAgentFlow,
		updateAgentFlow,
		getAgentFlowById,
		validateAgentFlow,
		exportAgentFlow,
		importAgentFlow,
		checkFlowIdAvailable,
		autoBuildAgentFlow,
		autoBuildFlowChat
	} from '$lib/apis/agent-flows';
	import type { AgentFlow, FlowData, AgentFlowForm } from '$lib/apis/agent-flows';
	import Spinner from '$lib/components/common/Spinner.svelte';

	// AI Chat Panel — Multi-turn ReAct with Human-in-the-Loop
	let showAIChat = false;
	let aiChatInput = '';
	let aiChatModel = '';
	let aiChatLoading = false;
	let aiFlowId = '';
	let aiChatHeight = 320; // Track generated flow for updates

	type ChatMsg = {
		role: 'user' | 'ai';
		content: string;
		loading?: boolean;
		buttons?: Array<{ label: string; value: string }>;
	};
	let aiChatMessages: ChatMsg[] = [];
	let conversationHistory: Array<{ role: string; content: string }> = [];
	let pendingTaskId = '';

	// Derive model options from store (base LLM models only)
	$: aiModelOptions = ($models ?? [])
		.filter((m: any) => m.id && m.name && !m?.info?.base_model_id && !m?.preset && m?.owned_by !== 'arena' && m?.owned_by !== 'agent_flow')
		.map((m: any) => ({ id: m.id, label: m.name }));

	// Load existing chat history when editing a flow
	function loadChatHistory(flowMeta: any) {
		if (flowMeta?.ai_chat_history) {
			conversationHistory = flowMeta.ai_chat_history;
			aiChatMessages = conversationHistory.map((m: any) => ({
				role: m.role === 'assistant' ? 'ai' as const : 'user' as const,
				content: m.content,
			}));
		}
	}

	function handleButtonClick(value: string) {
		console.log('[FlowEditor] Button clicked:', value);
		// Remove buttons from the last message to prevent re-clicking
		aiChatMessages = aiChatMessages.map(m =>
			m.buttons ? { ...m, buttons: undefined } : m
		);
		sendAIChat(value);
	}

	async function sendAIChat(overrideMsg?: string | Event) {
		// Ignore Event objects (from on:click handlers)
		const msg = typeof overrideMsg === 'string' ? overrideMsg.trim() : '';
		const userMsg = msg || aiChatInput.trim();
		if (!userMsg || aiChatLoading) return;
		aiChatInput = '';

		// Add to histories
		conversationHistory = [...conversationHistory, { role: 'user', content: userMsg }];
		aiChatMessages = [...aiChatMessages, { role: 'user', content: userMsg }];
		aiChatMessages = [...aiChatMessages, { role: 'ai', content: '', loading: true }];
		aiChatLoading = true;

		// API 호출을 글로벌 트래커에 등록 — 페이지 이동 후에도 toast 표시
		const promise = autoBuildFlowChat(
			localStorage.token,
			conversationHistory,
			aiChatModel,
			flowName || '',
			aiFlowId || flowId || ''
		);

		pendingTaskId = trackAIGeneration('flow', flowName || 'new', promise, {
			success: $i18n.t('Flow generated by AI'),
			error: $i18n.t('Flow AI generation failed'),
		});

		try {
			const result = await promise;

			// Add AI response to history
			const aiMsg = result.assistant_message || '';
			conversationHistory = [...conversationHistory, { role: 'assistant', content: aiMsg }];

			// Remove loading, add real message
			aiChatMessages = aiChatMessages.slice(0, -1);

			if (result.pending_input && result.pending_input.options?.length > 0) {
				// LLM is asking user — show buttons
				aiChatMessages = [...aiChatMessages, {
					role: 'ai',
					content: aiMsg,
					buttons: result.pending_input.options.map((o: string) => ({ label: o, value: o })),
				}];
			} else {
				aiChatMessages = [...aiChatMessages, { role: 'ai', content: aiMsg }];
			}

			// Update flow if generated
			if (result.flow_data && result.flow_data.nodes?.length > 0) {
				flowData = result.flow_data;
				flowData = { ...flowData };
				if (result.flow_name && !flowName) flowName = result.flow_name;
				if (result.flow_description && !flowDescription) flowDescription = result.flow_description;
				if (result.flow_id) aiFlowId = result.flow_id;
			}

			// toast는 trackAIGeneration에서 처리됨
			clearAITask(pendingTaskId);
		} catch (e: any) {
			aiChatMessages = aiChatMessages.slice(0, -1);
			aiChatMessages = [...aiChatMessages, {
				role: 'ai',
				content: `❌ ${e?.toString() || 'Error'}`,
			}];
			clearAITask(pendingTaskId);
		} finally {
			aiChatLoading = false;
			pendingTaskId = '';
		}
	}
	import { compressImage } from '$lib/utils';
	import { brandingUrls } from '$lib/stores/branding';
	import WorkspaceTagSelector from '$lib/components/common/WorkspaceTagSelector.svelte';
	import AccessControlModal from '$lib/components/workspace/common/AccessControlModal.svelte';

	import NodePalette from '$lib/components/workspace/AgentFlows/NodePalette.svelte';
	import NodeConfigPanel from '$lib/components/workspace/AgentFlows/NodeConfigPanel.svelte';
	import FlowToolbar from '$lib/components/workspace/AgentFlows/FlowToolbar.svelte';
	import Button from '$lib/components/common/Button.svelte';
	import Selector from '$lib/components/common/Selector.svelte';
	import LockClosed from '$lib/components/icons/LockClosed.svelte';

	// Dynamic import for FlowBuilder to avoid SSR issues with @xyflow/svelte
	let FlowBuilder: any = null;
	let Node: any = null;

	// Props
	export let flowId: string | null = null;
	export let mode: 'create' | 'edit' = 'create';

	// State
	let flow: AgentFlow | null = null;
	let flowName = '';
	let flowCustomId = '';
	let flowIdAvailable: boolean | null = null;
	let flowIdChecking = false;
	let flowDescription = '';
	let flowData: FlowData = { nodes: [], edges: [], variables: {} };
	let isActive = true;
	let profileImageUrl = $brandingUrls.favicon;

	let selectedNode: any = null;
	let upstreamNodes: any[] = [];
	let stateKeys: Array<{ key: string; source: string; nodeId: string; type: string }> = [];
	let isValid = true;
	let isSaving = false;
	let loaded = false;

	let accessControl: any = null;
	let showAccessControlModal = false;
	let tagSelector: any;

	let flowBuilderRef: FlowBuilder;
	let filesInputElement: HTMLInputElement;
	let inputFiles: FileList | null = null;

	// Handle image upload
	$: if (inputFiles && inputFiles.length > 0) {
		const reader = new FileReader();
		reader.onload = async (event) => {
			const originalSrc = event.target?.result as string;
			try {
				const compressedSrc = await compressImage(originalSrc);
				profileImageUrl = compressedSrc;
			} catch (e) {
				console.error('Failed to compress image:', e);
				profileImageUrl = originalSrc;
			}
			inputFiles = null;
		};
		reader.readAsDataURL(inputFiles[0]);
	}

	// Flow ID validation
	let flowIdCheckTimer: ReturnType<typeof setTimeout>;

	function handleFlowIdInput() {
		flowIdAvailable = null;
		clearTimeout(flowIdCheckTimer);

		const id = flowCustomId.trim().toLowerCase();
		if (!id || id.length < 2) return;

		if (!/^[a-z0-9][a-z0-9_-]*$/.test(id)) {
			flowIdAvailable = null;
			return;
		}

		flowIdChecking = true;
		flowIdCheckTimer = setTimeout(async () => {
			try {
				const result = await checkFlowIdAvailable(localStorage.token, id);
				flowIdAvailable = result.available;
			} catch {
				flowIdAvailable = null;
			} finally {
				flowIdChecking = false;
			}
		}, 400);
	}

	// Load flow if editing
	onMount(async () => {
		// Dynamically import FlowBuilder to avoid SSR issues
		if (browser) {
			const module = await import('$lib/components/workspace/AgentFlows/FlowBuilder.svelte');
			FlowBuilder = module.default;
		}

		if (mode === 'edit' && flowId) {
			try {
				flow = await getAgentFlowById(localStorage.token, flowId);
				if (flow) {
					flowName = flow.name;
					flowDescription = flow.description || '';
					flowData = flow.flow_data || { nodes: [], edges: [], variables: {} };
					isActive = flow.is_active;
					profileImageUrl = (flow.meta as any)?.profile_image_url || $brandingUrls.favicon;
					accessControl = flow.access_control ?? null;
					// Restore AI chat history if exists
					loadChatHistory(flow.meta);
				}
			} catch (e) {
				console.error('Failed to load flow:', e);
				toast.error($i18n.t('Failed to load flow'));
			}
		}
		loaded = true;
	});

	// Handlers
	async function handleSave() {
		if (!flowName.trim()) {
			toast.error($i18n.t('Please enter a flow name'));
			return;
		}

		// 생성 시 ID 필수 검증
		if (mode === 'create') {
			const id = flowCustomId.trim().toLowerCase();
			if (!id) {
				toast.error($i18n.t('Please enter a Flow ID'));
				return;
			}
			if (!/^[a-z0-9][a-z0-9_-]*$/.test(id)) {
				toast.error($i18n.t('Flow ID can only contain lowercase letters, numbers, and hyphens'));
				return;
			}
			if (id.length < 2 || id.length > 50) {
				toast.error($i18n.t('Flow ID must be between 2 and 50 characters'));
				return;
			}
			if (flowIdAvailable === false) {
				toast.error($i18n.t('This Flow ID is already in use'));
				return;
			}
		}

		isSaving = true;
		try {
			// 태그 변경사항 커밋 (save가 페이지 이동할 수 있으므로 먼저 실행)
			if (tagSelector?.commitChanges) {
				try {
					await tagSelector.commitChanges();
				} catch (e) {
					console.error('Failed to commit tag changes:', e);
				}
			}

			const currentFlowData = flowBuilderRef?.getFlowData() || flowData;

			const formData: AgentFlowForm = {
				id: flowCustomId.trim().toLowerCase(),
				name: flowName,
				description: flowDescription,
				flow_data: currentFlowData,
				is_active: isActive,
				access_control: accessControl,
				meta: {
					profile_image_url: profileImageUrl
				}
			};

			if (mode === 'create') {
				const newFlow = await createAgentFlow(localStorage.token, formData);
				if (newFlow) {
					toast.success($i18n.t('Flow created successfully'));
					goto(`/workspace/flows/${newFlow.id}`);
				}
			} else if (flowId) {
				const updatedFlow = await updateAgentFlow(localStorage.token, flowId, formData);
				if (updatedFlow) {
					flow = updatedFlow;
					toast.success($i18n.t('Flow saved successfully'));
				}
			}
		} catch (e) {
			console.error('Failed to save flow:', e);
			toast.error($i18n.t('Failed to save flow'));
		} finally {
			isSaving = false;
		}
	}

	async function handleValidate() {
		const currentFlowData = flowBuilderRef?.getFlowData() || flowData;

		try {
			const result = await validateAgentFlow(localStorage.token, currentFlowData);
			isValid = result.valid;

			if (result.valid) {
				toast.success($i18n.t('Flow is valid'));
			} else {
				const errorMessages = result.errors.map((e) => e.message).join('\n');
				toast.error($i18n.t('Validation errors') + ':\n' + errorMessages);
			}

			if (result.warnings.length > 0) {
				const warningMessages = result.warnings.map((w) => w.message).join('\n');
				toast.warning($i18n.t('Warnings') + ':\n' + warningMessages);
			}
		} catch (e) {
			console.error('Validation failed:', e);
			toast.error($i18n.t('Validation failed'));
		}
	}

	async function handleExport() {
		if (!flowId) {
			// Export current unsaved flow
			const currentFlowData = flowBuilderRef?.getFlowData() || flowData;
			const exportData = {
				version: '1.0',
				type: 'agent_flow',
				name: flowName || 'Untitled Flow',
				description: flowDescription,
				flow_data: currentFlowData
			};

			const blob = new Blob([JSON.stringify(exportData, null, 2)], { type: 'application/json' });
			const url = URL.createObjectURL(blob);
			const a = document.createElement('a');
			a.href = url;
			a.download = `${flowName || 'flow'}.json`;
			a.click();
			URL.revokeObjectURL(url);
			return;
		}

		try {
			const exportData = await exportAgentFlow(localStorage.token, flowId);
			const blob = new Blob([JSON.stringify(exportData, null, 2)], { type: 'application/json' });
			const url = URL.createObjectURL(blob);
			const a = document.createElement('a');
			a.href = url;
			a.download = `${flowName || 'flow'}.json`;
			a.click();
			URL.revokeObjectURL(url);
			toast.success($i18n.t('Flow exported successfully'));
		} catch (e) {
			console.error('Export failed:', e);
			toast.error($i18n.t('Export failed'));
		}
	}

	function handleImport() {
		const input = document.createElement('input');
		input.type = 'file';
		input.accept = '.json';
		input.onchange = async (e) => {
			const file = (e.target as HTMLInputElement).files?.[0];
			if (!file) return;

			try {
				const text = await file.text();
				const data = JSON.parse(text);

				if (data.type !== 'agent_flow') {
					toast.error($i18n.t('Invalid flow file'));
					return;
				}

				// Update local state with imported data
				flowName = data.name || flowName;
				flowDescription = data.description || flowDescription;
				flowData = data.flow_data || { nodes: [], edges: [], variables: {} };

				toast.success($i18n.t('Flow imported successfully'));
			} catch (e) {
				console.error('Import failed:', e);
				toast.error($i18n.t('Failed to import flow'));
			}
		};
		input.click();
	}

	function handleBack() {
		goto('/workspace/flows');
	}

	function handleNodeSelect(event: CustomEvent<{ node: any; upstreamNodes?: any[]; stateKeys?: typeof stateKeys }>) {
		selectedNode = event.detail.node;
		upstreamNodes = event.detail.upstreamNodes || [];
		stateKeys = event.detail.stateKeys || [];
	}

	function handleNodeUpdate(event: CustomEvent<{ nodeId: string; data: any }>) {
		flowBuilderRef?.updateNodeData(event.detail.nodeId, event.detail.data);
	}

	function handleNodeDelete(event: CustomEvent<{ nodeId: string }>) {
		flowBuilderRef?.deleteNode(event.detail.nodeId);
		selectedNode = null;
	}

	function handleFlowChange(event: CustomEvent<{ flowData: FlowData }>) {
		flowData = event.detail.flowData;
	}
</script>

