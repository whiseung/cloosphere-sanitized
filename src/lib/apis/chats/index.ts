// HITL (Human-in-the-Loop) — UnifiedAgent 가 도구 실행 직전에 interrupt 로 멈추면
// 클라이언트는 socket.io hitl_request 이벤트를 받는다. 사용자 결정 후 이 함수로
// 같은 그래프를 깨워 stream 을 이어간다. 응답은 SSE (text/event-stream).
export type HITLDecision = {
	type: 'approve' | 'edit' | 'reject' | 'respond';
	edited_action?: { name?: string; args?: Record<string, unknown> };
	message?: string;
};

export const resumeChatHITL = async (
	token: string,
	chatId: string,
	body: {
		thread_id: string;
		decisions: HITLDecision[];
		payload: Record<string, unknown>;
		chain_run_id?: string | null;
		trace_id?: string | null;
	}
): Promise<Response> => {
	// SSE 응답이라 res.body 를 createOpenAITextStream 으로 처리. fetch Response 그대로 반환.
	const res = await fetch(`${WEBUI_API_BASE_URL}/chats/${chatId}/resume`, {
		method: 'POST',
		headers: {
			Accept: 'text/event-stream',
			'Content-Type': 'application/json',
			...(token && { authorization: `Bearer ${token}` })
		},
		body: JSON.stringify(body)
	});

	if (!res.ok) {
		let detail = `HTTP ${res.status}`;
		try {
			const errBody = await res.json();
			detail = errBody?.detail ?? detail;
		} catch {
			// noop — body 가 비어있거나 JSON 아닐 때
		}
		throw detail;
	}

	return res;
};
