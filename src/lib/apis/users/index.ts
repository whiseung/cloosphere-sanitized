export type UsageByModelEntry = {
	model_id: string;
	name: string;
	used: number;
	limit: number; // 0 = 무제한 / 한도 없음
	source: string;
	is_agent: boolean;
};

export const getUserUsageByModel = async (
	token: string,
	userId: string,
	period: 'today' | 'week' | 'month' = 'today'
): Promise<UsageByModelEntry[]> => {
	let error = null;
	const url = new URL(`${WEBUI_API_BASE_URL}/users/${userId}/usage-by-model`);
	url.searchParams.set('period', period);
	const res = await fetch(url.toString(), {
		method: 'GET',
		headers: {
			Accept: 'application/json',
			authorization: `Bearer ${token}`
		}
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			console.log(err);
			error = err.detail;
			return null;
		});
	if (error) throw error;
	return res || [];
};
